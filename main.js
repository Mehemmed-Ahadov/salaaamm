let tasks = [];
let completedTasks = 0;

function addTask() {
    let taskInput = document.getElementById('taskInput').value;
    if (taskInput !== '') {
        tasks.push({ name: taskInput, completed: false });
        document.getElementById('taskInput').value = '';
        renderTasks();
    }
}

function renderTasks() {
  let taskList = document.getElementById('taskList');
  taskList.innerHTML = '';
  tasks.forEach((task, index) => {
      let li = document.createElement('li');
      li.innerHTML = `<input type="checkbox" onclick="toggleTask(${index})" ${task.completed ? 'checked' : ''}>
                      ${task.name} <button onclick="deleteTask(${index})">x</button>`;
      li.className = task.completed ? 'completed' : '';
      taskList.appendChild(li);
  });
  updateStatus();
}

function toggleTask(index) {
  tasks[index].completed = !tasks[index].completed;
  tasks[index].completed ? completedTasks++ : completedTasks--;
  renderTasks();
}
function deleteTask(index) {
  if (tasks[index].completed) completedTasks--;
  tasks.splice(index, 1);
  renderTasks();
}

function removeChecked() {
  tasks = tasks.filter(task => !task.completed);
  completedTasks = 0;
  renderTasks();
}

function updateStatus() {
  document.getElementById('taskStatus').innerText = `${completedTasks} of ${tasks.length} `;
}
